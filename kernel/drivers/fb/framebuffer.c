/* ── fb_init ──────────────────────────────────────────────────────────────── */

void fb_init(u32 *addr, u32 width, u32 height, u32 pitch, u32 bpp) {
    fb_addr   = addr;
    fb_width  = width;
    fb_height = height;
    fb_pitch  = pitch;
    fb_bpp    = bpp;
    fb_ready  = true;
}

/* ── Pixel operations ─────────────────────────────────────────────────────── */

void fb_draw_pixel(u32 x, u32 y, u32 color) {
    if (!fb_ready || x >= fb_width || y >= fb_height) return;
    u32 offset = y * (fb_pitch / 4) + x;
    fb_addr[offset] = color;
}

void fb_fill_rect(u32 x, u32 y, u32 w, u32 h, u32 color) {
    if (!fb_ready) return;
    u32 x_end = MIN(x + w, fb_width);
    u32 y_end = MIN(y + h, fb_height);
    u32 stride = fb_pitch / 4;
    for (u32 row = y; row < y_end; row++) {
        u32 *line = fb_addr + row * stride + x;
        for (u32 col = x; col < x_end; col++)
            *line++ = color;
    }
}

void fb_blit(u32 dst_x, u32 dst_y, u32 *src, u32 w, u32 h) {
    if (!fb_ready) return;
    u32 stride = fb_pitch / 4;
    for (u32 row = 0; row < h && (dst_y + row) < fb_height; row++) {
        u32 *dst_line = fb_addr + (dst_y + row) * stride + dst_x;
        u32 *src_line = src + row * w;
        for (u32 col = 0; col < w && (dst_x + col) < fb_width; col++)
            dst_line[col] = src_line[col];
    }
}

/* ── Scroll up by N pixel rows ───────────────────────────────────────────── */

void fb_scroll_up(u32 lines) {
    if (!fb_ready || lines >= fb_height) return;
    u32 stride  = fb_pitch / 4;
    u32 src_row = lines;
    u32 copy_rows = fb_height - lines;

    /* Move rows upward */
    u32 *dst = fb_addr;
    u32 *src = fb_addr + lines * stride;
    for (u32 r = 0; r < copy_rows; r++) {
        for (u32 c = 0; c < stride; c++)
            dst[c] = src[c];
        dst += stride;
        src += stride;
    }
    /* Clear bottom */
    for (u32 r = 0; r < lines; r++) {
        for (u32 c = 0; c < stride; c++)
            dst[c] = 0;
        dst += stride;
    }
    (void)src_row;
}

void fb_clear(u32 color) {
    fb_fill_rect(0, 0, fb_width, fb_height, color);
}

/* ── Draw a character using the 8×16 bitmap font ─────────────────────────── */

void fb_draw_char_bitmap(u32 x, u32 y, char c, u32 fg, u32 bg) {
    if (!fb_ready) return;

    const u8 *glyph;
    u32 idx = (u8)c;

    if (idx >= 0x20 && idx < 0x80 && (idx - 0x20) < (int)ARRAY_SIZE(font8x16))
        glyph = font8x16[idx - 0x20];
    else
        glyph = box_char;

    u32 stride = fb_pitch / 4;
    for (u32 row = 0; row < 16 && (y + row) < fb_height; row++) {
        u8 bits = glyph[row];
        u32 *line = fb_addr + (y + row) * stride + x;
        for (u32 col = 0; col < 8 && (x + col) < fb_width; col++) {
            line[col] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}
